        [AllowAnonymous]
        public ActionResult Register(string redirecturl = null)
        {
            return View(new RegisterViewModel() {RedirectUrl = redirecturl});
        }

        //
        // POST: /Account/Register
        [HttpPost]
        [ValidateAntiForgeryToken]
        [AllowAnonymous]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user	= new ApplicationUser { UserName = model.Email, Email = model.Email, AccountType = "Listener" };
                var result	= await UserManager.CreateAsync(user, model.Password);

                if (result.Succeeded)
                {
                    //await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);

					UserRepo.SetupNewUserAndSave(user.Id);

					// For more information on how to enable account confirmation and password reset please visit http://go.microsoft.com/fwlink/?LinkID=320771
					// Send an email with this link

					string code			= await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                    string callbackUrl	= Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code, redirect = model.RedirectUrl }, protocol: Request.Url.Scheme);
                    await UserManager.SendEmailAsync(user.Id, Settings.SiteName + " Account Activation", "Welcome to "+Settings.SiteName+"! <br><br>Please open the following URL in your browser to verify your email address and activate your account. <br><br> <a href=\"" + callbackUrl + "\">" + callbackUrl + "</a>");

					return View("VerifyEmail");
                }

                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

        //
        // GET: /Account/ConfirmEmail
        [AllowAnonymous]
        public async Task<ActionResult> ConfirmEmail(string userId, string code, string redirect)
        {
            if (userId == null || code == null)
            {
                return View("Error");
            }

            var result = await UserManager.ConfirmEmailAsync(userId, code);

			if (result.Succeeded)
			{
				ApplicationUser user = UserRepo.VerifyUserEmailAndSave(userId);
				SignInManager.SignIn(user, isPersistent: false, rememberBrowser: false);
			}
			else
				return View("Error");

			if (!string.IsNullOrWhiteSpace(redirect) && redirect.StartsWith("/") && !redirect.StartsWith("//"))
				return View(new ConfirmEmailViewModel() {RedirectUrl = redirect});

			return View();
        }

