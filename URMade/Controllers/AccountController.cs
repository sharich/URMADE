        //
        // GET: /Account/ResetPassword
        [AllowAnonymous]
        public ActionResult ResetPassword(string e, string t)
        {
            if (!SecurityHelper.TokenValid(t, e))
            {
                return View("Error");
            }

            ViewBag.e = e;
            ViewBag.t = t;

            return View();
        }

        //
        // POST: /Account/ResetPassword
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public ActionResult ResetPassword(ResetPasswordViewModel model, string e, string t)
        {
            var user = SecurityHelper.FindUserByToken(t, e);
            if (user == null)
            {
                return View("Error");
            }

            ViewBag.e = e;
            ViewBag.t = t;

            if (!ModelState.IsValid)
            {
                return View(model);
            }

            SecurityHelper.UpdatePassword(user.Id, model.Password);

            return RedirectToAction("ResetPasswordConfirmation", "Account");
        }

